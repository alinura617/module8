def compare_inputs():
    # Get two inputs from the user
    input1 = input("Enter the first input: ")
    input2 = input("Enter the second input: ")
    
    # Compare the inputs and print the result
    if input1 == input2:
        print("The inputs are equal.")
    else:
        print("The inputs are not equal.")

# Call the function to compare inputs
compare_inputs()
